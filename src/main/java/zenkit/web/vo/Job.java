package zenkit.web.vo;

import java.util.Date;

public class Job {
	private int level;
	private int j_no;
	private int j_refno;
	private String j_name;
	private String j_startD;
	private String j_endD;
	private double j_completer;
	
	
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getJ_no() {
		return j_no;
	}
	public void setJ_no(int j_no) {
		this.j_no = j_no;
	}
	public int getJ_refno() {
		return j_refno;
	}
	public void setJ_refno(int j_refno) {
		this.j_refno = j_refno;
	}
	public String getJ_name() {
		return j_name;
	}
	public void setJ_name(String j_name) {
		this.j_name = j_name;
	}
	public String getJ_startD() {
		return j_startD;
	}
	public void setJ_startD(String j_startD) {
		this.j_startD = j_startD;
	}
	public String getJ_endD() {
		return j_endD;
	}
	public void setJ_endD(String j_endD) {
		this.j_endD = j_endD;
	}
	public double getJ_completer() {
		return j_completer;
	}
	public void setJ_completer(double j_completer) {
		this.j_completer = j_completer;
	}
	
	
}
